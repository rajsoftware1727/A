import java.util.*;
public class SwitchCase {
	public static void main(String args[])
	{
		Scanner reader=new Scanner(System.in);
		System.out.println("Enter N value");
		int N=reader.nextInt();
		switch(N)
		{
		case (1):
		
		System.out.println("Your are entered one");
		break;
		case (2):
			System.out.println("Your are entered two");
		break;
		case (3):
			System.out.println("Your are entered three");
		break;
		 
			

		
			
		
		}
		
	}

}
